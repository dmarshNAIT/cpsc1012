namespace DataTypeSandbox
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // bool is short for Boolean
            // these variables can only be true or false
            bool isWinter = true;
            bool isSummer = false;
            bool hasValue = true;

            // char is short for "character"
            // it holds a SINGLE character
            // we use SINGLE quotes around the value
            char menuChoice = '1';
            char menuChoiceV2 = 'Q';
            char punctuation = '$';
            char blankSpace = ' ';

            // string is a "string of 0+ characters"
            string userName = "Bob";
            string emptyString = "";
            string message = "Welcome to my awesome program.";
        }
    }
}
